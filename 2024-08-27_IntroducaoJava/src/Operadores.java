
public class Operadores {
    //psvm + TAB [main]
    //sout + TAB [System.out.println]
    public static void main(String[] args) {
        int x = 5;
        int y = 10;
        int z = 20;
        
        //Atribuição    ==> [=, +=, -=, *=, /=, %=, ++, --]
        //Aritmeticos   ==> [+, -, *, /, %]
        //Relacionais   ==> [>, <, >=, <=, ==, !=]
        //logicos       ==> [&&, ||, !]
        
        //Atribuicao
        x += 3;
        System.out.println(x);
        y *= 5;
        System.out.println(y);
        
        //Aritmeticos        
        int a = z + 5;
        System.out.println(a);
    }
}
