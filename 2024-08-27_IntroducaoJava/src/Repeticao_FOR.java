
public class Repeticao_FOR {
    public static void main(String[] args) {
        //Imprimir os numeros entre 0 e 10
        System.out.print("Print de 0 a 10 ==> ");
        for(int i = 0; i <= 10; i++){
            System.out.print(i + " ");
        }
        System.out.println("");
        System.out.print("Print de 10 a 0 ==> ");
        
        for(int i = 10; i >= 0; i--){
            System.out.print(i + " ");
        }
    }
}
