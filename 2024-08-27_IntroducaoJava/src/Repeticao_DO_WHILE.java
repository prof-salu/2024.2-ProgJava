public class Repeticao_DO_WHILE {
    public static void main(String[] args) {
        //Imprimir os numeros entre 0 e 10
        System.out.print("Print de 0 a 10 ==> ");
        int i = 1;
        
        do{
            System.out.print(i + " ");
            i++;
        }while(i <= 10);
    }
}
