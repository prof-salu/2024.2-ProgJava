public class Repeticao_WHILE {
    public static void main(String[] args) {
        //Imprimir os numeros entre 0 e 10
        System.out.print("Print de 0 a 10 ==> ");
        int i = 0;
        
        while(i <= 10){
            System.out.print(i + " ");
            i++;
        }
    }
}
